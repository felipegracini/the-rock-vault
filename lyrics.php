            $(document).on('click', '.btnSearchLyrics', function()
            {
                var artSong = $('input[name=txt_art_sng]').val();

                $(".div_lyrics").show();

                if(artSong != null && artSong != '' && artSong != '*' && artSong != ' ')
                {
                    $(".div_lyrics .div_art_song").each(function(i, lyric)
                    {
                        if(!(lyric.innerHTML.toLowerCase().indexOf(artSong.toLowerCase()) >= 0))
                            $(this).parent().parent().hide();
                    });
                }
            });
